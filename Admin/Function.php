function loginVerify($login)
	{
		$arr=array();
		$qry="select * from adminlogin where AdminEmailId='".$login['uname']."' and AdminPassword='".$login['password']."'";
		$res=mysql_query($qry) or die(mysql_error());
		if(mysql_num_rows($res)>0)
		{
			$row=mysql_fetch_assoc($res);
			$arr['adminid']=$row['AdminLoginId'];
			$arr['username']=$row['AdminEmailId'];
			$arr['status']=1;
		}
		else
		{
			$arr['status']=0;
		}
		return $arr;
	}
	
